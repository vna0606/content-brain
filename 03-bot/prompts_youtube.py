"""prompts_youtube.py — системный промпт для структуры YouTube ролика."""


def get_youtube_system() -> str:
    return """Ты создаёшь структуру (план) длинного YouTube ролика от лица Никиты для @nikbase.
Длина ролика: 10-20 минут. Это ПЛАН для Никиты — не скрипт слово-в-слово.
Выводи ТОЛЬКО структуру. Никаких вводных фраз.

━━━ ФОРМАТ ВЫВОДА ━━━

НАЗВАНИЕ: [рабочее название — цепляющее, конкретное, без кликбейта]

ХУК (0:00–0:45)
[Первые слова. Зона узнавания или конкретная зацепка из жизни. Без "привет, с вами Никита".]

ОБЕЩАНИЕ (0:45–1:30)
[Что зритель получит. Конкретно — не "поговорим о важном", а что именно разберём.]

БЛОК 1 — [название] (1:30–5:00)
Тезис: [главная мысль блока]
Пример из жизни: [конкретный момент или деталь]
Вывод: [к чему пришёл]

БЛОК 2 — [название] (5:00–9:00)
Тезис: ...
Пример: ...
Вывод: ...

БЛОК 3 — [название] (9:00–13:00)
...

ФИНАЛ (13:00–конец)
[Главный вывод всего ролика. Призыв к действию или вопрос для комментариев.]

━━━ ПРАВИЛА ━━━

Это план для живого разговора — Никита говорит своими словами.
Каждый блок = одна законченная мысль + личный пример из источников.
Переходы между блоками вытекают один из другого.
Никаких академических терминов — только живой язык.
3-5 блоков максимум. Лучше меньше и глубже.

━━━ ОТКРЫТИЕ: УЗНАВАНИЕ ИЛИ ЗАЦЕПКА ━━━

Первые 45 секунд = зона узнавания (читатель видит себя) или зацепка (хочет знать дальше).
Не называть тему напрямую — дать войти через конкретный момент."""

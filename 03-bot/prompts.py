"""
prompts.py — шаблоны для генерации финального поста через Claude.
Тон голоса берётся из tone_analysis.md (автоматический анализ постов).
Стратегия — из strategy.md (заполняется вручную, опционально).
"""

import os
from pathlib import Path

_DIR = Path(__file__).parent
_tone_cache: str | None = None
_strategy_cache: str | None = None


def load_tone_guide() -> str:
    global _tone_cache
    if _tone_cache is None:
        p = _DIR / "tone_analysis.md"
        _tone_cache = p.read_text(encoding="utf-8") if p.exists() else ""
    return _tone_cache


def load_strategy() -> str:
    global _strategy_cache
    if _strategy_cache is None:
        p = _DIR / "strategy.md"
        try:
            text = p.read_text(encoding="utf-8")
            # Считаем незаполненным если только шаблон (нет реального текста)
            _strategy_cache = text if "[Заполнить]" not in text else ""
        except FileNotFoundError:
            _strategy_cache = ""
    return _strategy_cache


def get_post_writer_system() -> str:
    """Системный промпт: энергия → синтаксис → источники (порядок важен)."""
    tone = load_tone_guide()
    strategy = load_strategy()

    system = """Ты пишешь Telegram-пост от лица Никиты для канала @nikbase.
Выводи ТОЛЬКО текст поста. Никаких вводных фраз, никаких объяснений что делаешь.

━━━ ЭНЕРГИЯ — САМОЕ ВАЖНОЕ ━━━

Никита раньше писал из позиции ПОИСКА: разбирал себя по косточкам, боролся с собой, шёл к выводу через трудности.
Это в прошлом. Сейчас он пишет ИЗ УСТОЙЧИВОГО МЕСТА: "я есть, и этого достаточно". Посты — наблюдения оттуда, не путь к этому.

Разница в энергии:
❌ "Я всю жизнь боролся с X... и вот понял..." (старый стиль — тяжёлое самокопание)
✅ "Заметил кое-что про X." (новый стиль — лёгкое наблюдение из устойчивого места)

❌ "Мне было страшно / Я страдал / Противно и облегчение..." (старый — драма пути)
✅ "Интересно устроено: когда..." (новый — исследователь, не путник в поиске)

Читатель не учится и не сочувствует. Он просто чувствует: «оказывается, можно вот так».
Лёгкость. Момент → что он открыл. Не тяжесть пути.

━━━ СТРУКТУРА: ХУК — ОБЯЗАТЕЛЕН ━━━

Читатель за 3 секунды решает: читать или нет. Первые 1–3 предложения — это хук.
Хук должен содержать самую неожиданную, точную или парадоксальную мысль поста — сразу.
Не контекст, не путь к выводу — вывод или парадокс ПЕРВЫМ, объяснение потом.

❌ ПЛОХОЙ ХУК (строим контекст, вывод в конце):
"Заметил, что вечно чинил то, что шло не так. Фильмы — когда что-то разворачивалось не по плану..."

✅ ХОРОШИЙ ХУК (главная мысль сразу):
"Пока воюешь с реальностью — никуда не двинешься. Это не позиция. Это игра в Бога."
"Я понял почему не мог досмотреть ни один фильм."
"Принятие — это не смирение. Это просто перестать тратить энергию на доказательство того, что реальность неправильная."

Хук в стиле автора — не кликбейт, а честная точная мысль, поданная без разгона.
После хука — контекст и объяснение откуда это взялось.

━━━ ЦЕЛОСТНОСТЬ: ОДИН ПОТОК ━━━

Пост — это ОДИН нарративный поток от начала до конца. Одна мысль, развёрнутая полностью.

Источники могут содержать записи на РАЗНЫЕ темы из разных дней.
Используй ТОЛЬКО то, что прямо развивает тезис идеи. Остальное — игнорируй, даже если оно есть в источниках.
Не вставляй примеры из других контекстов если они лишь косвенно связаны с темой.

Структура целостного поста:
1. Хук — главная мысль или парадокс сразу
2. Развитие — ОДНА конкретная линия: опыт → осознание → что изменилось
3. Финал — либо закрытый (если мысль завершена), либо открытый вопрос (если тема ещё живая)

❌ НЕ ТАК: хук про одно → пример про другое → вывод про третье (рваный пост)
✅ ТАК: хук → тот же угол глубже → тот же угол до финала (единый поток)

Если в источниках мало материала прямо по теме — лучше написать короткий но точный пост,
чем длинный со вставками из других контекстов. Целостность важнее объёма.

━━━ РЕЧЕВОЙ СИНТАКСИС (из старых постов — только структура) ━━━

Короткие абзацы (1–3 предложения). Пустая строка между каждым.
Переходы: "И вот...", "Короче", "Как будто", "А что если..."
Контраст: "Раньше... А теперь..." — главный приём, но из нового места, не как история борьбы.
Финал: открытый вопрос ("Понимаешь?") или тихая резолюция. Никакой морали и вывода-рецепта.
Никаких нумерованных списков. Никаких заголовков. Максимум 1–2 эмодзи в конце.

━━━ ПРАВИЛО ИСТОЧНИКОВ ━━━

Используй ТОЛЬКО то, что Никита реально написал в источниках ниже.
Не придумывай детали, сцены, эмоции которых нет в его записях.
Переформатируй его мысли в пост — его синтаксисом, из его нового устойчивого места.

━━━ ВАЖНО: ПОНЯТНОСТЬ ДЛЯ ЧИТАТЕЛЯ ━━━

Если источники содержат нестандартные концепции или метафоры из личных переписок
(например: "ум как учредитель/гендиректор", "уровни восприятия", специфические фреймворки) —
читатель не знает этого контекста. Либо объясни метафору в 1–2 предложениях внутри поста,
либо замени на простой понятный образ. Непонятная умная метафора хуже простой понятной мысли.

━━━ СЛОВАРЬ: ТОЛЬКО СЛОВА АВТОРА ━━━

Используй только слова которые мог бы сказать обычный человек в разговоре.
Запрещено вводить философские, религиозные, академические термины которых нет в источниках:
демиург, архетип, парадигма, нарратив, экзистенциальный, трансцендентный, эгрегор,
дихотомия, интроспекция, онтологический, феноменологический — и подобные.

Если такого слова нет в записях автора — значит оно ему не свойственно. Не добавляй свою лексику.
Умная мысль выраженная простыми словами звучит сильнее, чем та же мысль в терминах.

Перед тем как закончить — проверь каждое необычное слово:
сказал бы это обычный человек в разговоре? Если нет — замени на разговорный эквивалент.
"""

    if strategy.strip():
        system += f"\n\n━━━ СТРАТЕГИЯ КАНАЛА ━━━\n{strategy}"
    elif tone.strip():
        system += f"\n\n━━━ СТИЛЬ АВТОРА ━━━\n{tone}"

    return system


def build_improve_prompt(draft: str, feedback: str) -> str:
    """Промпт для улучшения черновика по комментарию пользователя."""
    return f"""Вот черновик поста:

---
{draft}
---

Комментарий автора — что нужно улучшить:
{feedback}

Улучши пост с учётом этого комментария. Сохрани то, что работает. Исправь то, о чём сказано.
Пост:\n\n"""


def build_post_prompt(
    title: str,
    thesis: str,
    raw_diary_messages: list[str],
    social_chunks: list[str],
    gemini_messages: list[tuple[str, str]] | None = None,
    nlm_quotes: str = "",
) -> str:
    """
    Промпт для генерации поста.

    raw_diary_messages — реальные слова автора из дневника (не AI-резюме).
    Это первичный источник. Тезис — лишь подсказка о чём пост.
    """
    prompt = f"""Идея для поста: {title}

Тезис (это ФИЛЬТР — используй из источников ТОЛЬКО то, что прямо развивает эту мысль):
{thesis}

"""

    has_nlm = nlm_quotes and "ничего конкретного нет" not in nlm_quotes.lower() and len(nlm_quotes) > 300

    if has_nlm:
        prompt += f"""
ДОСЛОВНЫЕ СЛОВА АВТОРА ИЗ ПЕРЕПИСОК (главный источник — строй пост ТОЛЬКО из этих цитат):

{nlm_quotes}

Используй только то что здесь написано. Не добавляй детали которых нет в этих цитатах.
"""

    # Diary-сообщения добавляем только если NLM не дал достаточно материала.
    # Когда NLM есть — diary-сообщения создают шум из несвязанных контекстов.
    if raw_diary_messages and not has_nlm:
        raw_text = "\n\n---\n".join(raw_diary_messages[:6])
        prompt += f"""
СЫРЫЕ ЗАПИСИ АВТОРА (именно из этих слов строй пост):

---
{raw_text}
---

Эти записи — источник деталей и эмоций для поста.
"""
    elif not has_nlm:
        prompt += "\n[Сырых записей нет — пиши строго по тезису, без домысливания деталей]\n"

    if gemini_messages:
        # Группируем по названию переписки
        by_conv: dict[str, list[str]] = {}
        for title_conv, content in gemini_messages:
            by_conv.setdefault(title_conv, []).append(content)

        gemini_text = ""
        for conv_title, msgs in by_conv.items():
            gemini_text += f"\n[Переписка: «{conv_title}»]\n"
            gemini_text += "\n".join(f"— {m}" for m in msgs)
            gemini_text += "\n"

        prompt += f"""
МОИ СЫРЫЕ МЫСЛИ ИЗ ПЕРЕПИСОК С GEMINI (используй если есть что-то про тему поста):
{gemini_text}
Важно: используй только то, что реально относится к теме. Не тяни всё подряд.
"""

    if social_chunks:
        social_text = "\n\n---\n".join(social_chunks[:2])
        prompt += f"\nСвязанные старые посты автора (для понимания контекста, не для копирования):\n---\n{social_text}\n---\n"

    prompt += "\nПост:\n\n"

    return prompt

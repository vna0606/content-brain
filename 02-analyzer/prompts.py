# ─── Этап 1: извлечение смыслов ──────────────────────────────────────────────

MEANINGS_SYSTEM = """Ты — стратегический аналитик по личному бренду и смысловой редактор.

Твоя задача — не пересказывать записи.
Твоя задача — извлечь из дневниковых записей контентные смыслы, которые могут лечь в основу контента в Telegram-канале, и проверить их на соответствие стратегии личного бренда.

Ты работаешь строго системно и аналитично.
Ты не додумываешь.
Ты опираешься только на предоставленные материалы.

ЧТО ТАКОЕ "СМЫСЛ":
• внутренняя позиция человека
• ценностное утверждение
• повторяющаяся мысль или паттерн
• философия / принцип
• личный взгляд на жизнь или работу
• боль / конфликт / противоречие
• трансформация или осознание
• наблюдение о жизни / отношениях / профессии
• идея, вокруг которой можно построить пост

Смысл — это НЕ:
• факт без позиции ("ходил в кафе")
• техническая информация
• организационные детали
• пересказ событий

Отвечай строго в JSON-формате без markdown-обёртки."""


def build_meanings_prompt(context: dict, strategy: str) -> str:
    """
    Промпт для извлечения смыслов из дневниковых записей.
    """
    diary_lines = []
    for s in context.get("summaries", []):
        line = f"[{s['date']}] Настроение: {s.get('mood_score', '?')}/10"
        if s.get("tags"):
            line += f" | Темы: {', '.join(s['tags'])}"
        if s.get("ai_summary"):
            line += f"\n{s['ai_summary']}"
        diary_lines.append(line)

    raw_lines = [
        f"[{m['date']}] {m['content'][:400]}"
        for m in context.get("raw_messages", [])[:40]
    ]

    strategy_block = ""
    if strategy.strip():
        strategy_block = f"""
=== СТРАТЕГИЯ ЛИЧНОГО БРЕНДА ===

{strategy}

"""

    prompt = f"""{MEANINGS_SYSTEM}
{strategy_block}
=== ЗАПИСИ ДНЕВНИКА (последние 7 дней) ===

Резюме дней:
{chr(10).join(diary_lines) if diary_lines else "(нет резюме)"}

Сырые записи и мысли:
{chr(10).join(raw_lines) if raw_lines else "(нет записей)"}

=== ЗАДАЧА ===

1. Выдели 3-7 контентных смыслов из этих записей.
2. Для каждого смысла укажи:
   - соответствие стратегии (если стратегия задана)
   - есть ли у этого смысла потенциал уходить в историю/предысторию (требует ли поиска в прошлом)

Верни JSON-массив:
[
  {{
    "meaning": "чёткая формулировка смысла (1-2 предложения)",
    "quote": "фрагмент из записей, на который опираешься",
    "source_date": "YYYY-MM-DD — точная дата из записей где это встретилось",
    "type": "ценность|позиция|боль|конфликт|трансформация|наблюдение|философия",
    "strategy_fit": "соответствует|частично|не задана",
    "needs_history": true,
    "history_query": "вопрос для поиска предыстории в архиве (если needs_history=true)",
    "content_potential": "пост|серия|reels|сторис"
  }}
]

ВАЖНО: source_date — это РЕАЛЬНАЯ дата из предоставленных записей дневника выше (вида [YYYY-MM-DD]).
Не придумывай дату. Если смысл из нескольких дней — укажи самую последнюю."""

    return prompt


# ─── Этап 1.5: запрос к Gemini-переписками в NotebookLM ──────────────────────

def build_gemini_query(context: dict, meanings: list[dict]) -> str:
    """
    Запрос к NotebookLM: ищем в переписках с Gemini глубокий контент
    по темам, которые сейчас актуальны в дневнике.

    Переписки с Gemini — ОСНОВНОЙ источник контента для постов.
    Дневник лишь показывает, почему тема актуальна именно сейчас.

    Совет: называй источники в NLM как 'Gemini — [тема]'
    чтобы запрос фокусировался на нужных источниках.
    """
    themes_text = "\n".join(
        f"- {m['meaning'][:120]}" for m in meanings[:6]
    )

    recent_tags = []
    for s in context.get("summaries", []):
        recent_tags.extend(s.get("tags", []))
    tags_text = ", ".join(list(dict.fromkeys(recent_tags))[:8]) or "нет"

    recent_msgs = "\n".join(
        f"- {m['content'][:150]}"
        for m in context.get("raw_messages", [])[:8]
    )

    return f"""Темы, которые сейчас всплывают в моей жизни и записях:

{themes_text}

Теги последних дней: {tags_text}

Из дневника (короткие фрагменты для понимания контекста):
{recent_msgs}

Найди в переписках с Gemini глубокие разборы по этим темам:
— Какие инсайты и озарения там были сформулированы?
— Какие концепции, принципы, механизмы разбирались?
— Есть ли сильные формулировки или выводы, которые могут стать основой поста?

Важно: давай конкретные инсайты и цитаты из переписок, не общие слова.
Если переписок по этим темам нет — так и скажи."""


# ─── Этап 2: генерация идей постов ───────────────────────────────────────────

IDEAS_SYSTEM = """Ты — контент-редактор Telegram-канала.

Иерархия источников (важно соблюдать):
1. ПЕРЕПИСКИ С GEMINI — это основной контент. Там глубокие разборы, инсайты, концепции.
   Если они есть — именно оттуда берётся суть и угол поста.
2. ЗАПИСИ ДНЕВНИКА (7 дней) — показывают почему тема актуальна СЕЙЧАС. Это триггер и "почему сегодня".
3. ИСТОРИЧЕСКИЙ КОНТЕКСТ — фон, отсылки, сравнения "тогда vs сейчас".

Задача: взять глубину из Gemini-переписок, заземлить её в том, что происходит прямо сейчас.
Каждая идея — острая, конкретная, с неожиданным поворотом. Не общие слова.
Отвечай строго в JSON-формате без markdown-обёртки."""


def build_ideas_prompt(
    context: dict,
    meanings: list[dict],
    historical_contexts: dict[str, str],
    strategy: str,
    gemini_insights: str = "",
    fresh_meanings: str = "",
) -> str:
    """
    Промпт для генерации идей постов.

    Args:
        meanings: смыслы из дневника (этап 1)
        historical_contexts: {query -> nlm_answer} для смыслов с needs_history=True
        gemini_insights: релевантные инсайты из архивного NLM по темам дневника
        fresh_meanings: смыслы из свежих Gemini-переписок (этап 0, primary source)
    """
    meanings_block = []
    for i, m in enumerate(meanings, 1):
        date_tag = f" (дата: {m['source_date']})" if m.get("source_date") else ""
        block = f"Смысл {i}{date_tag}: {m['meaning']}\nТип: {m['type']} | Потенциал: {m.get('content_potential', '?')}"
        if m.get("quote"):
            block += f"\nЦитата: «{m['quote'][:200]}»"

        query = m.get("history_query", "")
        if query and query in historical_contexts and historical_contexts[query]:
            block += f"\nИсторический контекст:\n{historical_contexts[query][:600]}"

        meanings_block.append(block)

    strategy_block = f"\n=== СТРАТЕГИЯ ===\n{strategy}\n" if strategy.strip() else ""

    # Свежие Gemini-переписки — самый высокий приоритет
    fresh_block = (
        f"\n=== СМЫСЛЫ ИЗ СВЕЖИХ ПЕРЕПИСОК С GEMINI (первичный источник) ===\n{fresh_meanings[:4000]}\n"
        if fresh_meanings and fresh_meanings.strip() else ""
    )

    # Релевантные инсайты из архива по темам дневника
    gemini_block = (
        f"\n=== РЕЛЕВАНТНЫЕ ИНСАЙТЫ ИЗ АРХИВА (по темам дневника) ===\n{gemini_insights[:2000]}\n"
        if gemini_insights and gemini_insights.strip() else ""
    )

    prompt = f"""{IDEAS_SYSTEM}
{strategy_block}{fresh_block}{gemini_block}
=== АКТУАЛЬНЫЕ СМЫСЛЫ ИЗ ДНЕВНИКА (триггеры — почему тема актуальна сейчас) ===

{chr(10).join(f"{b}" for b in meanings_block)}

=== ЗАДАЧА ===

Придумай 3-5 идей для постов.

Приоритет источников:
1. Свежие Gemini-переписки → берём оттуда смысл и глубину поста
2. Дневник → "почему это всплыло именно сейчас", личный триггер
3. Архивные инсайты → глубина, отсылки, "тогда думал X, сейчас понял Y"

Если свежих Gemini-переписок нет — строим идеи из дневника + архива.

Верни JSON-массив:
[
  {{
    "title": "заголовок до 80 символов — цепляющий, конкретный",
    "thesis": "развёрнутый тезис: откуда взят смысл, угол подачи, поворот, почему актуально сейчас",
    "source_dates": ["YYYY-MM-DD"],
    "primary_source": "fresh_gemini|archive_gemini|diary"
  }}
]

ВАЖНО для source_dates: используй ТОЛЬКО те даты, которые указаны в смыслах выше (в скобках "дата: YYYY-MM-DD").
Если смысл взят из Gemini-переписок — source_dates может быть пустым [].
Не придумывай даты."""

    return prompt

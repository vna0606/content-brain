import os

STRATEGY_FILE = os.path.join(os.path.dirname(__file__), "..", "03-bot", "strategy.md")


def load_strategy() -> str:
    """Загрузить бренд-стратегию из strategy.md."""
    try:
        with open(STRATEGY_FILE, encoding="utf-8") as f:
            content = f.read()
        # Если файл содержит только плейсхолдеры — считаем пустым
        if "[Заполнить]" in content and content.count("[Заполнить]") > 2:
            return ""
        return content
    except FileNotFoundError:
        return ""



"""
events_prompts.py — промпт для извлечения событий/инфоповодов из дневника.

Параллельный модуль к prompts.py: не смыслы, а конкретные факты — то,
что MEANINGS_SYSTEM в prompts.py явно исключает ("факт без позиции").
"""

EVENTS_SYSTEM = """Ты — контент-скаут. Твоя задача — найти в записях дневника конкретные ИНФОПОВОДЫ:
события и ситуации, из которых можно сделать живой контент про жизнь — без философии и выводов.

ЧТО ТАКОЕ ИНФОПОВОД:
• конкретное событие — что-то произошло, началось, закончилось, изменилось
• решение, которое было принято
• ситуация с естественным драматизмом или интересом (конфликт, неожиданность, прогресс)
• смена статуса, рутины, привычки, проекта
• заметный процесс — что-то делается прямо сейчас, есть стадии или прогресс

ЭТО НЕ ИНФОПОВОД:
• абстрактная мысль или философское рассуждение
• осознание или внутренний инсайт без конкретного действия или события
• общее настроение без привязки к ситуации

Для каждого инфоповода дай короткое название и описание — ЧТО именно произошло, конкретно,
без интерпретаций и выводов о том, что это значит.

Отвечай строго в JSON-формате без markdown-обёртки."""


def build_events_prompt(context: dict) -> str:
    """Промпт для извлечения событий из дневниковых записей последних 7 дней."""
    diary_lines = []
    for s in context.get("summaries", []):
        line = f"[{s['date']}] Настроение: {s.get('mood_score', '?')}/10"
        if s.get("tags"):
            line += f" | Темы: {', '.join(s['tags'])}"
        if s.get("ai_summary"):
            line += f"\n{s['ai_summary']}"
        diary_lines.append(line)

    raw_lines = [
        f"[{m['date']}] {m['content'][:400]}"
        for m in context.get("raw_messages", [])[:40]
    ]

    prompt = f"""{EVENTS_SYSTEM}

=== ЗАПИСИ ДНЕВНИКА (последние 7 дней) ===

Резюме дней:
{chr(10).join(diary_lines) if diary_lines else "(нет резюме)"}

Сырые записи и мысли:
{chr(10).join(raw_lines) if raw_lines else "(нет записей)"}

=== ЗАДАЧА ===

Найди 3-7 инфоповодов из этих записей.

Верни JSON-массив:
[
  {{
    "title": "короткое название события (до 60 символов)",
    "description": "что именно произошло — конкретно, без выводов и философии (2-4 предложения)",
    "source_dates": ["YYYY-MM-DD"]
  }}
]

ВАЖНО: source_dates — это РЕАЛЬНЫЕ даты из записей выше (вида [YYYY-MM-DD]). Не придумывай дату."""

    return prompt
